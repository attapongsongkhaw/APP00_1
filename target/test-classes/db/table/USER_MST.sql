DROP TABLE IF EXISTS USER_MST;
CREATE TABLE USER_MST (
  USER_ID varchar(120) NOT NULL,
  FIRST_NAME varchar(100) NOT NULL,
  LAST_NAME varchar(100) NOT NULL,
  CORP_CODE varchar(35) NOT NULL,
  CLIENT_CODE varchar(20) DEFAULT NULL,
  USER_GROUP varchar(4) NOT NULL,
  USER_ROLE varchar(2) DEFAULT NULL,
  LOGGED_ON char(1) NOT NULL,
  LOCKOUT_COUNTER INT NOT NULL,
  LAST_LOGGED_ON datetime DEFAULT NULL,
  LAST_LOGGED_OFF datetime DEFAULT NULL,
  ACTIVE_STATUS char(1) NOT NULL,
  AUTHORIZE_STATUS char(1) NOT NULL,
  REJECT_REASON varchar(255) DEFAULT NULL,
  OPERATION_FLAG char(1) NOT NULL,
  UMM_USER_STATUS char(1) DEFAULT NULL,
  UMM_SERVICE_STATUS char(1) DEFAULT NULL,
  UMM_SYNC_DATE datetime DEFAULT NULL,
  CREATED_DATE datetime NOT NULL,
  CREATED_BY varchar(20) NOT NULL,
  AUTHORIZED_DATE datetime DEFAULT NULL,
  AUTHORIZED_BY varchar(20) DEFAULT NULL,
  LAST_UPDATED_DATE datetime NOT NULL,
  LAST_UPDATED_BY varchar(20) NOT NULL,
  PRIMARY KEY (USER_ID)
);
