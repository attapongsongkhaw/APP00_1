DROP TABLE IF EXISTS CORPORATION_MST;
CREATE TABLE CORPORATION_MST (
  CORP_CODE varchar(20) NOT NULL,
  CORP_NAME varchar(255) NOT NULL,
  CIS_ID varchar(12) DEFAULT NULL,
  TAXPAYER_NO varchar(13) DEFAULT NULL,
  TAXPAYER_NAME varchar(255) DEFAULT NULL,
  CHARGE_FLAG char(1) DEFAULT NULL,
  CHARGE_ACCOUNT varchar(10) DEFAULT NULL,
  CHARGE_TYPE varchar(2) DEFAULT NULL,
  CHARGE_FREQ varchar(2) DEFAULT NULL,
  CHARGE_DATE char(2) DEFAULT NULL,
  CHARGE_AMOUNT double DEFAULT NULL,
  ACTIVE_STATUS char(1) NOT NULL,
  AUTHORIZE_STATUS char(1) NOT NULL,
  REJECT_REASON varchar(255) DEFAULT NULL,
  OPERATION_FLAG char(1) NOT NULL,
  CREATED_DATE datetime NOT NULL,
  CREATED_BY varchar(20) NOT NULL,
  AUTHORIZED_DATE datetime DEFAULT NULL,
  AUTHORIZED_BY varchar(20) DEFAULT NULL,
  LAST_UPDATED_DATE datetime NOT NULL,
  LAST_UPDATED_BY varchar(20) NOT NULL,
  UMM_COMPANY_STATUS CHAR(1) DEFAULT NULL,
  UMM_SERVICE_STATUS CHAR(1) DEFAULT NULL,
  UMM_SYNC_DATE DATETIME DEFAULT NULL,
  PRIMARY KEY (CORP_CODE)
);

